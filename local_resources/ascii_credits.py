if platform.system() != "Windows":  # compatibility fixes
    clear_command = "clear"
else:
    clear_command = "cls"

def run_credits(with_color=False,timeDuration=2.5):
    if with_color:#===== Color Credits
        system(clear_command)
        colorama.init()
        print(colorama.Fore.BLUE + developed_by)
        sleep(timeDuration)
        system(clear_command)
        print(colorama.Fore.RED + andrew_mistele)
        sleep(timeDuration)
        system(clear_command)
        print(colorama.Fore.MAGENTA + kyle_mistele)
        sleep(timeDuration)
        system(clear_command)
        print(colorama.Fore.BLUE + michael_naguib + colorama.Fore.WHITE)
        colorama.deinit()
        sleep(timeDuration)
        system(clear_command)
    else:#====== Plain Credits
        system(clear_command)
        print(developed_by)
        sleep(timeDuration)
        system(clear_command)
        print(andrew_mistele)
        sleep(timeDuration)
        system(clear_command)
        print(kyle_mistele)
        sleep(timeDuration)
        system(clear_command)
        print(michael_naguib)
        sleep(timeDuration)
        system(clear_command)